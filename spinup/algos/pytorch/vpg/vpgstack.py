        Dvfp = grad(loss_pi, ac.v.parameters())
        loss_v_s = compute_loss_v_surr(data)
        loss_v = compute_loss_v(data)
        Dpfv = grad(loss_v_s, ac.pi.parameters())
        Dvfv = grad(loss_v, ac.v.parameters())
